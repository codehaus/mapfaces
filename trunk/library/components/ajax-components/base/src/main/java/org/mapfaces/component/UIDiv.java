package org.mapfaces.component;

import javax.faces.context.FacesContext;

/**
 *
 * @author Mehdi Sidhoum (Geomatys)
 * @since 0.2
 */
public class UIDiv extends UIWidgetBase {

    public static final String FAMILIY = "org.mapfaces.Div";

    private String style = "";
    
    private String styleClass = "";

    public UIDiv() {
        super();
        setRendererType("org.mapfaces.renderkit.html.Div");    // this component has a renderer
    }

    /**
     * {@inheritDoc }
     */
    @Override
    public String getFamily() {
        return FAMILIY;
    }

    /**
     * {@inheritDoc }
     */
    @Override
    public Object saveState(final FacesContext context) {
        final Object values[] = new Object[4];
        values[0] = super.saveState(context);
        values[1] = getStyle();
        values[2] = getStyleClass();

        return values;
    }

    /**
     * {@inheritDoc }
     */
    @Override
    public void restoreState(final FacesContext context, final Object state) {
        final Object values[] = (Object[]) state;
        super.restoreState(context, values[0]);
        setStyle((String) values[1]);
        setStyleClass((String) values[2]);

    }

    /**
     * @return the style
     */
    public String getStyle() {
        return style;
    }

    /**
     * @param style the style to set
     */
    public void setStyle(String style) {
        this.style = style;
    }

    /**
     * @return the styleClass
     */
    public String getStyleClass() {
        return styleClass;
    }

    /**
     * @param styleClass the styleClass to set
     */
    public void setStyleClass(String styleClass) {
        this.styleClass = styleClass;
    }
}
